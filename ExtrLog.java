public class ExtrLog {
    private String traceID;
    private String stdFileNm;
    private String regYmd;
    private String gbmCd;

    public String getTraceID() {
        return traceID;
    }

    public void setTraceID(String traceID) {
        this.traceID = traceID;
    }

    public String getStdFileNm() {
        return stdFileNm;
    }

    public void setStdFileNm(String stdFileNm) {
        this.stdFileNm = stdFileNm;
    }

    public String getRegYmd() {
        return regYmd;
    }

    public void setRegYmd(String regYmd) {
        this.regYmd = regYmd;
    }

    public String getGbmCd() {
        return gbmCd;
    }

    public void setGbmCd(String gbmCd) {
        this.gbmCd = gbmCd;
    }
}
